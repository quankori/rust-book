// Topic: Flow Control using if..else
//
// Requirements:
// * Display a message based on the value of a boolean
// * When true: "hello"
// * When false: "goodbye"
//
// Notes:
// * Use a variable set to either true or false
// * Use an if..else block to determine what message to display
// * Use a function to display the message

fn main() {
    // * Display a message based on the value of a boolean
    let boolean = true;
    // * Use a function to display the message
    if boolean {
        println!("hello");
    } else {
        println!("goodbye");
    }
}
